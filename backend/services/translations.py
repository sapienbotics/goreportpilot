def get_translations(language_code: str) -> dict:
    """Get translation dict for a language. Falls back to English."""
    return TRANSLATIONS.get(language_code, TRANSLATIONS["en"])


def t(language_code: str, key: str) -> str:
    """Get a single translated string."""
    translations = get_translations(language_code)
    return translations.get(key, TRANSLATIONS["en"].get(key, key))


